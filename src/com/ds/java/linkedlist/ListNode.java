package com.ds.java.linkedlist;

public class ListNode {
	
	    int val; 
	    ListNode next;
	    
	    public ListNode(int data1) {
	        this.val = data1;
	        this.next = null;
	    }

	    ListNode(int data1,ListNode next1) {
	        this.val = data1;
	        this.next = next1;
	    }
	    
	   
	    

}
